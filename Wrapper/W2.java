class  W2
{
	public static void main(String[] args) 
	{
		Integer a=10;//Boxing Implicitly
		Double b=10.0;//Boxing Implicitly
		Integer c=Integer.valueOf(10);//Boxing Explicitly
		Double d=Double.valueOf(20.0);//Boxing Explicitly
		System.out.println("Hello World!");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

	}
}
